function decToHex(value) {
	if(value == null || isNaN(value) || value < 0 || value > 255)
		return null;
	
	var mod = value % 16,
		c1 = mod < 10 ? mod + "" : String.fromCharCode(55+mod),
		div = (value - mod) / 16,
		c2 = div < 10 ? div + "" : String.fromCharCode(55+div);
		
	return c2 + c1;
}

function refreshUI() {
	a.value = (a.value.charAt(0) == "." ? "0" : "") + a.value;
	if(isNaN(a.value)) {
		alert("Valeur incorrecte pour la transparence, les valeurs valides vont de 0 à 1 (Exemple: 0.75)");
		a.value = ""
	}
	var vr = parseInt(r.value),
		vg = parseInt(g.value),
		vb = parseInt(b.value),
		va = (a.value == "" || isNaN(a.value)) ? null : parseFloat(a.value),
		hr = decToHex(vr),
		hg = decToHex(vg),
		hb = decToHex(vb),
		ihr = decToHex(255-vr),
		ihg = decToHex(255-vg),
		ihb = decToHex(255-vb),
		hex = hr+hg+hb,
		shex = "#" + hex
		ha = va == null ? null : decToHex(Math.ceil(va * 255)),
		srgb = "rgb(" + vr + "," + vg + "," + vb + (va != null ? "," + va : "") + ")",
		shexa = "#" + hr + hg + hb + (ha != null ? ha : ""),
		scolor = couleurs[hex] ? couleurs[hex] : "";
		
	
	colorname.textContent = "";
//	colorname.style.backgroundColor = vr+vg+vb > 375 && scolor != "" ? "black" : "white";
	colorname.style.backgroundColor = "#" + ihr + ihg + ihb;
	colorname.style.color = shex;
	colorname.textContent = scolor;
	hexR.textContent = hr;
	hexG.textContent = hg;
	hexB.textContent = hb;
	hexA.textContent = (ha != null ? ha : "");
	hexresult.textContent = shexa;
	rgbresult.textContent = srgb;
	view.style.backgroundColor = shexa;
}

